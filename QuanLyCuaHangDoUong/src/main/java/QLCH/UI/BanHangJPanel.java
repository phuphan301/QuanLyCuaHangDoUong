    private void fillComBoxLoaiSanPham() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiSanPham.getModel();
        model.removeAllElements();
        List<LoaiSanPham> list = lspd.selectAll();
        for (LoaiSanPham lsp : list) {
            model.addElement(lsp);
        }
    }

    private void fillTableSanPham() {
        DefaultTableModel model = (DefaultTableModel) tblLoaiSanPham.getModel();
        model.setRowCount(0);
        try {
            LoaiSanPham lsp = (LoaiSanPham) cboLoaiSanPham.getSelectedItem();
            List<SanPham> list = bhd.selectBySanPham(lsp.getMaLoaiSP());
//            List<SanPham> list = spd.selectAll();
            for (SanPham sp : list) {
                Object[] row = {
                    sp.getMaSP(),
                    sp.getTenSP(),
                    sp.getGia(),
                    sp.getTrangThai(),
                    sp.getAnh(),
                    sp.getMaLoai(),
                    sp.getSoLuong()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MessageBoxQLCH.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    private void chonCombox() {
        LoaiSanPham lsp = (LoaiSanPham) cboLoaiSanPham.getSelectedItem();
        if (lsp != null) {
            fillTableSanPham();
        }
    }

