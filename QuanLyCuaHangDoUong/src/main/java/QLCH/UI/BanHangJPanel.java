    public static String TinhTongTien(JTable tbl) {

        double tongtien = 0;
        for (int i = 0; i < tbl.getRowCount(); i++) {
            tongtien += Double.parseDouble(tbl.getValueAt(i, 4).toString());
        }

        return String.valueOf(tongtien);
    }

